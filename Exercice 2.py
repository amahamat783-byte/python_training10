#Un algorithme qui  calcul le volume d'un parallélépipède rectangle dont sont fournis au depart la hauteur, la largeur et la profondeur.
h=int(input("Saisissez la valeur de la hauteur: "))
l=int(input("Saisissez la valeur de la largeur : "))
p=int(input("Saisissez la valeur de la profondeur: "))
V=h*l*p
print("le  volume votre parallélépipède est:", V)
